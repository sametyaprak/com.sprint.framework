package javaBrains2;

public class Point {

    private int pointA;
    private int pointB;
    private int pointC;
    private int pointD;

    public Point() {
    }

    public Point(int pointA, int pointB, int pointC) {
        this.pointA = pointA;
        this.pointB = pointB;
        this.pointC = pointC;
    }

    public int getPointA() {
        return pointA;
    }

    public void setPointA(int pointA) {
        this.pointA = pointA;
    }

    public int getPointB() {
        return pointB;
    }

    public void setPointB(int pointB) {
        this.pointB = pointB;
    }

    public int getPointC() {
        return pointC;
    }

    public void setPointC(int pointC) {
        this.pointC = pointC;
    }

    public int getPointD() {
        return pointD;
    }

    public void setPointD(int pointD) {
        this.pointD = pointD;
    }
}
